public class Dimensions {

    private int width;
    private int height;
    private int dept;

    public Dimensions(int width, int height, int dept) {
        this.width = width;
        this.height = height;
        this.dept = dept;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDept() {
        return dept;
    }

}
